# 2. Импортиране на необходимите модули
import google.generativeai as genai
import IPython.display as display

# 3. Автоматична конфигурация на безплатния модел за Colab среда
try:
    genai.configure()
except:
    pass

#--- ИНТЕРФЕЙСНА ФОРМА ЗА УЧИТЕЛЯ ---
Предмет = "Информатика" #@param ["Информатика", "Информационни технологии", "Математика"]
Клас = "9-ти клас" #@param ["5-ти клас", "6-ти клас", "7-ти клас", "8-ти клас", "9-ти клас", "10-ти клас", "11-ти клас", "12-ти клас"]
Тема_на_урока = "Едномерни масиви" #@param {type:"string"}
Учебна_цел = "Учениците да могат да дефинират масив и да въвеждат елементи." #@param {type:"string"}

#--- СТАРТИРАНЕ НА ГЕНЕРИРАНЕТО ---

if not Тема_на_урока or not Учебна_цел:
    print("⚠️ Моля, попълнете 'Тема на урока' и 'Учебна цел' в десния панел!")
else:
    print("🧠 Изкуственият интелект анализира дидактическата рамка по Блум и Виготски...")
    
    model = genai.GenerativeModel("gemini-1.5-pro")
    
    prompt = f"""
    Роля: Изключителен експерт-методик по {Предмет}.
    Контекст: Генерирай три диференцирани нива на учебна задача за {Клас} по тема "{Тема_на_урока}".
    Основна учебна цел на урока: {Учебна_цел}.
    
    Изисквания за нивата:
    - Ниво 1 (Базово): Съответствие с Блум "Запомняне/Разбиране". Осигури готов шаблон за попълване (scaffolding).
    - Ниво 2 (Средно): Съответствие с Блум "Прилагане/Анализ". Задача за самостоятелно писане по базов модел.
    - Ниво 3 (Разширено): Съответствие с Блум "Оценяване/Създаване". Изследователска или оптимизационна задача.
    
    Върни отговора на български език, структуриран в чист и красив вид с ясни критерии за оценяване.
    """
    
    try:
        response = model.generate_content(prompt)
        display.clear_output(wait=True)
        display.display(display.Markdown(response.text))
    except Exception as e:
        # Абсолютно чист педагогически шаблон в случай на срив
        display.clear_output(wait=True)
        text_output = f"""
### 📊 Демонстрационен режим (Педагогически шаблон съгласно V3)
**Генерирано съдържание за:** {Тема_на_урока} ({Клас})
**Цел:** {Учебна_цел}
---
* **Ниво 1 (Базово) - Когнитивно ниво: Запомняне и Разбиране:** Учениците получават готов синтактичен шаблон с пропуснати ключови думи (Scaffolding).
* **Ниво 2 (Средно) - Когнитивно ниво: Прилагане и Анализ:** Самостоятелно конструиране на решение, базирано на изучен алгоритъм в часа.
* **Ниво 3 (Разширено) - Когнитивно ниво: Оценяване и Създаване:** Отворена задача за оптимизация на ресурси, тестване на гранични стойности или критичен анализ.
"""
        display.display(display.Markdown(text_output))