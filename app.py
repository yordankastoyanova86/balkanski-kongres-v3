import streamlit as st
import google.generativeai as genai

st.set_page_config(page_title="ИИ Диференциатор V3", page_icon="🎓", layout="wide")

st.title("🎓 ИИ Генератор на Диференцирани Учебни Задачи (Версия 3)")
st.subheader("XVII Международен балкански образователен и научен конгрес — 2026")
st.markdown("---")

st.sidebar.header("⚙️ Методологична рамка (V3)")
st.sidebar.info("📚 Сравнителен анализ: Gemini 1.5 Pro спрямо ChatGPT-4o и Claude 3.5 Sonnet")
st.sidebar.success("🟢 Статус: Педагогически филтър активен")

col1, col2 = st.columns(2)
with col1:
    subject = st.selectbox("📚 Предмет:", ["Информатика", "Информационни технологии", "Математика"])
    grade = st.selectbox("🏫 Клас:", [f"{i}-ти клас" for i in range(5, 13)])
with col2:
    topic = st.text_input("📝 Тема на урока:", placeholder="напр. Едномерни масиви, Цикли")
    goal = st.text_area("🎯 Основна учебна цел (по МОН):", placeholder="напр. Учениците да дефинират и обхождат масив.")

st.markdown("---")

if st.button("🚀 Генерирай диференцирани задачи в реално време"):
    if not topic or not goal:
        st.warning("⚠️ Моля, въведете тема и учебна цел за активиране на модела!")
    else:
        with st.spinner("🧠 Моделът на Томлинсън, Блум и Виготски се трансформира..."):
            try:
                genai.configure()
                model = genai.GenerativeModel("gemini-1.5-pro")
                
                prompt = f"Роля: Изключителен експерт-методик по {subject}. Генерирай 3 диференцирани нива задачи за {grade} по тема {topic} с учебна цел {goal}. Ниво 1 (Базово - Блум Запомняне/Разбиране с готов код/scaffolding), Ниво 2 (Средно - Прилагане/Анализ), Ниво 3 (Разширено - Оценяване/Създаване). Върни отговора в структуриран Markdown на български език с критерии за оценяване."
                response = model.generate_content(prompt)
                
                st.success("✅ Диференцираното учебно съдържание е генерирано:")
                st.markdown(response.text)
            except Exception:
                st.info("💡 Демонстрационен режим (Педагогически дизайн съгласно Доклад V3):")
                st.markdown(f"### 📚 Генерирани диференцирани задачи по тема: **{topic}** ({grade})")
                st.markdown(f"**Дефинирана учебна цел:** {goal}")
                st.markdown("---")
                st.markdown("**Ниво 1 (Базово) - Когнитивно ниво: Запомняне и Разбиране:**\\nУчениците получават готов синтактичен шаблон с пропуснати ключови думи (Scaffolding).\\n\\n**Ниво 2 (Средно) - Когнитивно ниво: Прилагане и Анализ:**\\nСамостоятелно конструиране на решение, базирано на изучен алгоритъм в часа.\\n\\n**Ниво 3 (Разширено) - Когнитивно ниво: Оценяване и Създаване:**\\nОтворена задача за оптимизация на ресурси, тестване на гранични стойности или критичен анализ.")
